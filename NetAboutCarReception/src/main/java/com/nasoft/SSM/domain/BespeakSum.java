package com.nasoft.SSM.domain;

import java.io.Serializable;

/**
 * @author skins
 *
 */
public class BespeakSum implements Serializable{
	private  Integer bespeakId;
	private Integer morningCount;
	private Integer afternoonCount;
	private Integer sum;
	private String bespeakDate;
	private String address;
	private String year;
	private String month;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public Integer getBespeakId() {
		return bespeakId;
	}
	public void setBespeakId(Integer bespeakId) {
		this.bespeakId = bespeakId;
	}
	public Integer getMorningCount() {
		return morningCount;
	}
	public void setMorningCount(Integer morningCount) {
		this.morningCount = morningCount;
	}
	public Integer getAfternoonCount() {
		return afternoonCount;
	}
	public void setAfternoonCount(Integer afternoonCount) {
		this.afternoonCount = afternoonCount;
	}
	public Integer getSum() {
		return sum;
	}
	public void setSum(Integer sum) {
		this.sum = sum;
	}
	public String getBespeakDate() {
		return bespeakDate;
	}
	public void setBespeakDate(String bespeakDate) {
		this.bespeakDate = bespeakDate;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	
	 
}
