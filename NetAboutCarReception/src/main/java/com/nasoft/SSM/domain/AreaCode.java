package com.nasoft.SSM.domain;

import java.io.Serializable;

public class AreaCode implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String areaCode;//行政区划代码
	  
	private String areaName;//行政区域名称
	  
	private String pareaCode;//上级区划代码
	  
	private String areaLevel;//区域等级
	  
	private String remark;//备注
	  
	private int createUserId;//创建人
	  
	private int createInstId;//创建机构
	  
	private String createDateTime;//创建时间
	  
	private int updUserId;//修改人
	  
	private int updInstId;//修改机构
	  
	private String updDateTime;//修改时间

	public String getAreaCode() {
		return areaCode;
	}

	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}

	public String getAreaName() {
		return areaName;
	}

	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	public String getPareaCode() {
		return pareaCode;
	}

	public void setPareaCode(String pareaCode) {
		this.pareaCode = pareaCode;
	}

	public String getAreaLevel() {
		return areaLevel;
	}

	public void setAreaLevel(String areaLevel) {
		this.areaLevel = areaLevel;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public int getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(int createUserId) {
		this.createUserId = createUserId;
	}

	public int getCreateInstId() {
		return createInstId;
	}

	public void setCreateInstId(int createInstId) {
		this.createInstId = createInstId;
	}

	public String getCreateDateTime() {
		return createDateTime;
	}

	public void setCreateDateTime(String createDateTime) {
		this.createDateTime = createDateTime;
	}

	public int getUpdUserId() {
		return updUserId;
	}

	public void setUpdUserId(int updUserId) {
		this.updUserId = updUserId;
	}

	public int getUpdInstId() {
		return updInstId;
	}

	public void setUpdInstId(int updInstId) {
		this.updInstId = updInstId;
	}

	public String getUpdDateTime() {
		return updDateTime;
	}

	public void setUpdDateTime(String updDateTime) {
		this.updDateTime = updDateTime;
	}
	
}
