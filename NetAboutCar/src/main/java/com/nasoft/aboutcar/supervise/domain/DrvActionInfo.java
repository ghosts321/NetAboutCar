package com.nasoft.aboutcar.supervise.domain;

import java.util.Date;
/**
 * 驾驶员执法行动信息表
 */
public class DrvActionInfo {
    private String drv_action_id;

    private String name;

    private String certno;

    private String illegal_content;

    private String areacode;

    private String enforcer;
    
    private String action_time;
    
    private String action_result;

	public String getDrv_action_id() {
		return drv_action_id;
	}

	public void setDrv_action_id(String drv_action_id) {
		this.drv_action_id = drv_action_id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCertno() {
		return certno;
	}

	public void setCertno(String certno) {
		this.certno = certno;
	}

	public String getIllegal_content() {
		return illegal_content;
	}

	public void setIllegal_content(String illegal_content) {
		this.illegal_content = illegal_content;
	}

	public String getAreacode() {
		return areacode;
	}

	public void setAreacode(String areacode) {
		this.areacode = areacode;
	}

	public String getEnforcer() {
		return enforcer;
	}

	public void setEnforcer(String enforcer) {
		this.enforcer = enforcer;
	}

	public String getAction_time() {
		return action_time;
	}

	public void setAction_time(String action_time) {
		this.action_time = action_time;
	}

	public String getAction_result() {
		return action_result;
	}

	public void setAction_result(String action_result) {
		this.action_result = action_result;
	}

	
}