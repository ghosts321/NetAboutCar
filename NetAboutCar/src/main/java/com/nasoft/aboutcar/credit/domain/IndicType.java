package com.nasoft.aboutcar.credit.domain;

import java.io.Serializable;

/**
 * 指标分类
 * @author Administrator
 *
 */
public class IndicType implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String type_name;//分类名称
	private String type_code;//分类代码
	private Integer type_level;//分类等级
	private String type_desc;//分类描述
	private String upper_type_code;//上级分类
	private String category;//1：驾驶员 2：平台 
	private Integer create_user;
	private String create_time;
	private Integer update_user;
	private String update_time;
	public String getType_name() {
		return type_name;
	}
	public void setType_name(String type_name) {
		this.type_name = type_name;
	}
	public String getType_code() {
		return type_code;
	}
	public void setType_code(String type_code) {
		this.type_code = type_code;
	}
	public Integer getType_level() {
		return type_level;
	}
	public void setType_level(Integer type_level) {
		this.type_level = type_level;
	}
	public String getType_desc() {
		return type_desc;
	}
	public void setType_desc(String type_desc) {
		this.type_desc = type_desc;
	}
	public String getUpper_type_code() {
		return upper_type_code;
	}
	public void setUpper_type_code(String upper_type_code) {
		this.upper_type_code = upper_type_code;
	}
	public Integer getCreate_user() {
		return create_user;
	}
	public void setCreate_user(Integer create_user) {
		this.create_user = create_user;
	}
	
	public Integer getUpdate_user() {
		return update_user;
	}
	public void setUpdate_user(Integer update_user) {
		this.update_user = update_user;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getUpdate_time() {
		return update_time;
	}
	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	
}
