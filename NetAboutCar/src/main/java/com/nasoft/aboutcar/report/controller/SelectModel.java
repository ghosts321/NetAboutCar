package com.nasoft.aboutcar.report.controller;

import java.io.Serializable;

public class SelectModel implements Serializable {

		/**
	 * 
	 */
	private static final long serialVersionUID = 167354701970225368L;
	
		private String code;
		private String name;
		private String pareaCode;
		private String areaCode;
		private String areaName;
		private String areaLevel;
		
		public String getCode() {
			return code;
		}
		public void setCode(String code) {
			this.code = code;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAreaCode() {
			return areaCode;
		}
		public void setAreaCode(String areaCode) {
			this.areaCode = areaCode;
		}
		public String getAreaName() {
			return areaName;
		}
		public void setAreaName(String areaName) {
			this.areaName = areaName;
		}
		public String getAreaLevel() {
			return areaLevel;
		}
		public void setAreaLevel(String areaLevel) {
			this.areaLevel = areaLevel;
		}
		public String getPareaCode() {
			return pareaCode;
		}
		public void setPareaCode(String pareaCode) {
			this.pareaCode = pareaCode;
		}
		
		
	
}
